#!/bin/bash

# Variables Declaration
x=10
y=2

# Multiplication
let multiply=$x*$y
let sum=$x+$y

echo "Multiply : $multiply and Addition : $sum"

# Calculation
subtraction=$(($x-$y))
division=$(($x/$y))
modulas=$((11%2))

# Displaying in the Terminal
echo -e "Subtraction : $subtraction\nDivision : $division\nModulas : $modulas"







